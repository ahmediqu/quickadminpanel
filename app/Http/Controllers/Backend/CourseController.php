    {
        return view('backend.course.create');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $update_id = $request->update_id;
        if(isset($update_id) && !empty($update_id)){
            $course =  Course::findOrFail($update_id);
        }else{
            $course = new Course();
        }
        $image = $request->file('image');
        if ($image) {
            $image_name = Auth::user()->id.time().'.'.request()->image->getClientOriginalExtension();


            $image_full_name = $image_name;
            $destination_path = 'uploads/course/';
            $image_url = $destination_path . $image_full_name;
            $success = $request->file('image')->move($destination_path, $image_full_name);
            if ($success) {
                $course->image = $image_url;
            }
        }

        $course->course_title = $request->course_title;
        $course->course_subtitle = $request->course_subtitle;
        $course->course_description = $request->course_description;
        $course->course_fee = $request->course_fee;
        $course->course_duration = $request->course_duration;
        
        $this->setSuccess('successfully created !!');
        $course->save();
        return back();
    }

